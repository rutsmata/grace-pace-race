import { useNavigate } from 'react-router-dom'

import * as articleAPI from '../../api/articleAPI'

import styles from './CreateArticle.module.css'
import useForm from '../../hooks/useForm'

export default function CreateArticle () {
    const navigate = useNavigate();

    const {formValues, changeHandler} = useForm({    
    author: '',
    title: '',
    description: '',
    img: '',
    type: ''
    })

    const submitHandler = async (e) => {
      e.preventDefault();

      try {
        await articleAPI.create(formValues)

      } catch (error) {
        console.log((error));
        
      }
      navigate('/articles')
    }

 
    return (
        <>              
              <div className={styles.createSection}>

                <form  method="post" className={styles.createForm}>
                  <h2>Create Article</h2>
                  <ul className={styles.noBullet}>
                  <li>
                      <label htmlFor="author">Author:</label>
                      <input
                        type="text"
                        className={styles.inputFields}
                        id="author"
                        placeholder="First Name & Last Name"
                        name="author"
                        value={formValues.author}
                        onChange={changeHandler}
                      />
                    </li>
                    <li>
                      <label htmlFor="title">Title:</label>
                      <input
                        type="text"
                        className={styles.inputFields}
                        id="title"
                        placeholder="Article Title"
                        name="title"
                        value={formValues.title}
                        onChange={changeHandler}
                      />
                    </li>
                    
                    <li>
                      <label htmlFor="type">Type:</label>
                      <select 
                          id="type" 
                          name="type" 
                          className={styles.inputFields} 
                          onChange={changeHandler} 
                          value={formValues.type}
                      >
                              <option  >Select Genre</option>
                                <option value='general' >General</option>
                                <option value='electric' >Electric</option>
                                <option value='hybrid'>Hybrid</option>
                                <option value='sport'>Sport</option>
                      </select>
                    </li>
                    <li>
                      <label htmlFor="description">Description:</label>
                      <textarea
                        id="description"
                        className={styles.inputFields}
                        name="description"
                        placeholder="Article Text Here..."
                        value={formValues.description}
                        onChange={changeHandler}
                      ></textarea>
                    </li>
                    <li>
                      <label htmlFor="img">Image:</label>
                      <input
                        type="text"
                        className={styles.inputFields}
                        id="img"
                        placeholder="http://..."
                        name="img"
                        value={formValues.img}
                        onChange={changeHandler}
                      />
                    </li>
                    <li id="center-btn">
                      <button className={styles['create-btn']} type="button" onClick={submitHandler}>Create</button>
                    </li>
                  </ul>
                </form>

              </div>
         </>
    )
}